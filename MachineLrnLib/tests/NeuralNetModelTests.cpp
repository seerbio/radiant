void NeuralNetModelTests::loadModelTest()
{
    const QString testModel =
        QDir(qApp->applicationDirPath()).filePath(QStringLiteral("rnn_linear_charge_w_precursors_nce_2.hdf5.json"));

    const QString testModelFail =
            QDir(qApp->applicationDirPath()).filePath(QStringLiteral("/Repositories/PyCharm/MSMS_RNN/epic_fail.json"));

    NeuralNetModel model;
    Err e = model.init(testModelFail);
    QCOMPARE(e, eError);

    e = model.init(testModel);
    QCOMPARE(e, eNoError);

}

